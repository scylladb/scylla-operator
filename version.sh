BRANCH=$(git rev-parse --abbrev-ref HEAD)
if [[ $BRANCH =~ ^v[0-9]+\.[0-9]+$ ]]; then
  echo $(git describe --tags --always --abbrev=0)
  exit 0
fi

echo 'nightly'